"""
core/cutting_macro.py — параметры макроса "Cutting" по образцу диалога
из Альфакама (Cutting v-5.1, см. скриншот пользователя).

Эта модель данных описывает ВЕСЬ запуск пакета обработки: один .ai → 
несколько .anc файлов (R, M1..MN, SV, corner, corner3D, revers_R).

Поля диалога Cutting v-5.1:

  Параметры ножа:
    Угол       — угол при вершине фрезы (60°, 70°, 90°)
    Пятка      — d_tip (диаметр кончика), типичные значения 0_6, 1_2
    Верх       — толщина листа сверху
    Низ        — фактическая глубина врезания (тот самый ProgZDepth)

  Направление:
    → / ↑↑     — направление обхода (по горизонтали / по вертикали)
    Включить реверс — генерировать также _revers_R файл

  Программ в одной линии:
    Кол-во     — сколько ножей в одном ряду на листе
    Ширина     — ширина одного ножа
    Длина      — длина листа

  Точка входа/выхода (отдельно для внутренней и внешней стороны):
    Угол       — Approach Angle (45° по умолчанию)
    Длина      — длина линейного участка захода
    Смещение по: ± число (для входа = -6 на внутреннем, -5 на внешнем)
    Перекрытие — overlap
    Быстро     — флаг ускорения
"""

from __future__ import annotations
from dataclasses import dataclass, field
from enum import Enum
from typing import List, Optional


class CutDirection(Enum):
    HORIZONTAL = "horizontal"  # программы идут строками (→)
    VERTICAL = "vertical"      # программы идут столбцами (↑↑)


@dataclass
class LeadInOutParams:
    """Параметры входа/выхода для одной стороны (Внутренняя или Внешняя)."""
    angle: float = 45.0       # Угол подхода/отхода
    length: float = 1.0       # Длина линейного участка
    offset: float = 0.0       # Смещение по (±)
    sign_offset: str = "+"    # знак (+/-) — для отображения в UI
    # Перекрытие после смыкания контура (мм). Фреза продолжает движение
    # вдоль контура ещё `overlap` мм после возврата в точку старта, прежде
    # чем уйдёт в lead-out. Нужно чтобы вход и выход не оказались в одной
    # точке (V-образное расхождение) и для поддерживающих тагов.
    overlap: float = 0.0
    # True если юзер ЯВНО задал offset (изменил поле в UI).
    # False — значение дефолтное, эмиттер может использовать автоподбор
    # позиции старта (если базовая не пробивает _check_lead).
    user_set_offset: bool = False


@dataclass
class CuttingMacroParams:
    """Полный набор параметров одного запуска Cutting-макроса.
    
    Это «инструкция оператора»: оператор открывает диалог, заполняет 
    поля, нажимает OK → программа генерирует пакет .anc файлов.
    """
    
    # ── Параметры ножа ──
    knife_angle: float = 70.0       # Угол (60/70/90)
    tip_diameter: float = 0.8       # Пятка (d_tip), мм
    top: float = 0.5                # Верх (толщина листа)
    bottom: float = 0.25            # Низ (ProgZDepth, глубина врезания)
    
    # ── Направление и реверс ──
    direction: CutDirection = CutDirection.HORIZONTAL
    enable_reverse: bool = True     # «Включить реверс» → доп. файл _revers_R
    
    # ── Группировка деталей в чистовые программы (_N_M.anc) ──
    # Группировка всегда по накопленной длине путей. Детали идут в порядке
    # обхода (определяется direction: горизонталь = по строкам, вертикаль =
    # по столбцам), накапливается длина, и при достижении лимита начинается
    # новая программа. Длинная строка делится, короткие — объединяются.
    # Лимит задаётся в единицах ГЕОМЕТРИИ (длина контура). Внутри умножается
    # на 2 (два прохода: внутр.+внешн.). Пользователь: 3000 геом = 6000 путей.
    max_geom_len: float = 3000.0
    # Допуск группировки координат в коридор (мм) — детали с близким Y (для
    # горизонтали) или X (для вертикали) считаются одной строкой/столбцом
    corridor_tolerance: float = 5.0
    
    # ── Программ в одной линии (раскладка на листе) ──
    programs_per_line: int = 1      # Кол-во
    program_width: float = 10.0     # Ширина
    sheet_length: float = 3000.0    # Длина листа
    
    # ── 2-я реперная точка (PT_PT_DIS) ──
    # X-координата второй реперной метки, мм. В .anc пишется как
    # N29 SSDE[SD.USR.Allign.DistC2C = X].
    fiducial_distance: float = 700.0
    
    # ── Точки входа/выхода ──
    # offset = смещение точки старта обхода вдоль контура от RT-угла bbox 
    # (отрицательно = назад по контуру). 
    # Эмпирически: 4мм от угла bbox по контуру даёт надёжный отступ от 
    # скруглений угла — заходные дуги не задевают лезвие.
    # Разница между INSIDE и OUTSIDE не критична (могут быть в одной точке).
    lead_inside: LeadInOutParams = field(default_factory=lambda: LeadInOutParams(
        angle=45.0, length=1.0, offset=-4.0, sign_offset="-"
    ))
    lead_outside: LeadInOutParams = field(default_factory=lambda: LeadInOutParams(
        angle=45.0, length=1.0, offset=-4.0, sign_offset="-"
    ))
    
    # ── Дополнительные опции ──
    overlap: float = 0.0            # Перекрытие
    fast_mode: bool = True          # Быстро
    
    # ── Что генерировать (опции пакета файлов) ──
    generate_rough_all: bool = True        # _R.anc — черновая всех элементов
    generate_finish_per_op: bool = True    # _N_M.anc — чистовые по операциям
    generate_reverse: bool = True          # _revers_R.anc — обратный обход
    generate_sv: bool = True               # _SV.anc — 4 угловых для сведения
    generate_corner: bool = True           # _corner.anc — тонкая фреза по углам
    generate_corner_3d: bool = False       # _corner3D.anc — 3D-фреза по углам

    # ── Режим «доработка» (_dop.anc) ──
    # Если активна (enabled) МЕНЬШАЯ доля ножей, чем dop_threshold_ratio,
    # чистовые файлы _N_M НЕ пишутся; вместо них один файл _dop.anc с
    # чистовым проходом только по активным ножам (для точечной доработки/
    # допила без перегенерации всего комплекта). 0.5 = меньше половины.
    dop_threshold_ratio: float = 0.5

    # ── Сглаживание под фрезу ──
    # Если включено, осевая каждого прохода сглаживается так, чтобы её
    # эквидистанта (offset фрезы) НЕ самопересекалась: тугие места, куда фреза
    # не входит, скругляются до проходимого радиуса; биарк-веера компенсации
    # убираются. Требует shapely. Применяется и в превью, и в .anc.
    smooth_offset_for_tool: bool = False

    # ── Сглаживание под фрезу ──
    # Если True — осевая каждого ножа сглаживается так, чтобы эквидистанта
    # (offset фрезы) НЕ самопересекалась: тугие места скругляются до
    # проходимого радиуса, биарк-кластеры убираются. Применяется и в превью,
    # и в .anc (по запросу — галка в UI). Требует shapely.
    smooth_offset_for_tool: bool = False

    # ── Сглаживание под фрезу ──
    # Если включено: осевая каждого ножа сглаживается так, чтобы эквидистанта
    # (offset фрезы) НЕ самопересекалась — тугие места скругляются до
    # проходимого радиуса, биарк-«веера» компенсации убираются. Применяется
    # при отрисовке путей и в экспорте. Требует shapely.
    smooth_offset_for_tool: bool = False
    
    # ── Авто-подбор lead-in/out для всех элементов ──
    # ВКЛ (default): применяется ко всем ножам где обнаружена коллизия 
    # захода/выхода с соседями или со своим контуром. Алгоритм:
    # сначала сдвиги позиции, затем варианты угла, затем укорачивание lead.
    # ВЫКЛ: только для выделенного элемента (логика пока не реализована,
    # будет добавлена когда появится механизм выделения в UI).
    auto_avoid_all: bool = True
    
    # ── Параметры детекции углов (для corner и corner3D) ──
    sharp_angle_threshold: float = 90.0      # (для 3D) угол меньше N° = острый
    corner_radius_threshold_mm: float = 0.7  # для 2D: если радиус скругления 
                                              # МЕНЬШЕ — нужна тонкая фреза
    corner_tool_number: int = 3              # T3 = пятка 0.6мм (углы)
    corner_3d_tool_number: int = 4           # T4 = 3D фреза (острые углы)
    
    # ── Имя выходных файлов (префикс) ──
    output_prefix: str = ""                # если пусто — берётся из .ai
    
    def expected_files(self) -> List[str]:
        """Возвращает список ожидаемых имён файлов на основе параметров.
        
        Имена в формате <prefix>_<angle>_<suffix>.anc, как на скриншоте:
            119220_80_all_R.anc
            119220_80_1_M.anc, 119220_80_2_M.anc, ...
            119220_80_revers_R.anc
            119220_80_SV.anc
            119220_80_corner.anc
            119220_80_corner3D.anc
        """
        prefix = self.output_prefix or "OUTPUT"
        angle_str = f"{int(self.knife_angle)}"
        files = []
        
        if self.generate_rough_all:
            files.append(f"{prefix}_{angle_str}_all_R.anc")
        if self.generate_reverse:
            files.append(f"{prefix}_{angle_str}_revers_R.anc")
        if self.generate_sv:
            files.append(f"{prefix}_{angle_str}_SV.anc")
        if self.generate_corner:
            files.append(f"{prefix}_{angle_str}_corner.anc")
        if self.generate_corner_3d:
            files.append(f"{prefix}_{angle_str}_corner3D.anc")
        # M-файлы — по количеству операций, имена будут заполнены при экспорте
        # (тут показываем шаблон, реальное количество зависит от .ai)
        if self.generate_finish_per_op:
            files.append(f"{prefix}_{angle_str}_<N>_M.anc")
        
        return files
